    public void mostrarCandidatos() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) Candidato_ComboBox.getModel();
        model.removeAllElements();
        Lista<Candidato>candidato=eleccion.getCandidatos();
        Nodo <Candidato> candi=candidato.getCabeza();
        model.addElement("----------");
        while(candi!=null){
            
            model.addElement(candi.getElemento().getNombre());
            candi=candi.getSgteNodo();
        }
    }
    public void activar_candidato(){
        Candidato_ComboBox.setEnabled(false);
        Tipo_voto_ComboBox.addActionListener(e->{
        String seleccion=(String)Tipo_voto_ComboBox.getSelectedItem();
            if("Voto efectivo".equals(seleccion)){
            Candidato_ComboBox.setEnabled(true);
            mostrarCandidatos();
        }else{
            Candidato_ComboBox.setEnabled(false);
        }
    });
    }


